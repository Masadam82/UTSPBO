import java.util.ArrayDeque;

public class Main {
    public static void main(String[] args) {
        ArrayDeque<Integer> deque = new ArrayDeque<>();

        deque.addFirst(1);
        deque.addLast(2);
        deque.addFirst(3);

        System.out.println("Elemen pertama: " + deque.removeFirst());
        System.out.println("Elemen terakhir: " + deque.removeLast());
    }
}
